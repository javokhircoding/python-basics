def avto_info(kompaniya, model, rangi, korobka, yili, narhi=None):
    """ Avtomobil haqidagi malumotlarni lug'at ko'rinishida qaytaruvchi funksiya"""
    avto = {'kompaniya':kompaniya,
            'model':model,
            'rangi':rangi,
            'korobka':korobka,
            'yil':yili,
            'narh':narhi}
    return avto

